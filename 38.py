# Afirmação 1
print("\n1. Um analista de consumo informa que a vida útil média de certo tipo de bateria automotiva não é de 74 meses.")
# a. Identifique a afirmação verbal e escreva-a como uma sentença matemática.
afirmacao_1 = "A vida útil média (μ) não é de 74 meses."
sentenca_mat_1 = "μ ≠ 74"
print(f"a. Afirmação: {afirmacao_1} (Matemática: {sentenca_mat_1})")
# b. Escreva o complemento da afirmação.
complemento_1 = "A vida útil média (μ) é de 74 meses."
complemento_mat_1 = "μ = 74"
print(f"b. Complemento: {complemento_1} (Matemática: {complemento_mat_1})")
# c. Identifique as hipóteses nula e alternativa e determine qual representa a afirmação.
H0_1 = "μ = 74"
Ha_1 = "μ ≠ 74"
print(f"c. H₀: {H0_1} (Hipótese Nula)")
print(f"   Hₐ: {Ha_1} (Hipótese Alternativa - REPRESENTA A AFIRMAÇÃO)")
print("   A afirmação (μ ≠ 74) não contém igualdade, então é Hₐ.")

